export function notificationBuilder(message, type = 'success') {
    return {
        message,
        type,
        info() {
            this.type = 'info'
            return this
        }
        , success() {
            this.type = 'success'
            return this
        },
        danger() {
            this.type = 'danger'
            return this
        }
        , warning() {
            this.type = 'warning'
            return this
        },
        setDelay(delay) {
            this.delay = delay
            return this
        },
        count: 100,
        wrapperId: null,
        Id: null,
        delay: 6000,
        getModalWrapper() {
            this.wrapperId = `little-admin-${Math.round(Math.random() * 100)}`
            return `<div class="wrapper-web-notification" id="little-admin-m-modal_wrapper"></div>`
        },
        widthVar : null,
        getModal() {
            this.Id = `little-admin-${Math.random().toString(36).slice(2)}`
            this.widthVar = '--' + this.Id + '-width'
            return `<div class="web-notification ${this.type}" id="${this.Id}" style="${this.widthVar}:100%" >
                        <div class=""> ${Icons()[this.type]} </div>
                        <div>${this.message}</div>
                        <span class="${this.type} h-1 absolute bottom-0 right-0 " style="width: var(${this.widthVar});background-color: inherit"></span>
                    </div>`
        },
        show() {
            document.getElementById('little-admin-m-modal_wrapper').insertAdjacentHTML('beforeend', this.getModal())
            document.getElementById(this.Id).classList.add('animate-notif')
            let i = 1
            const  el = document.getElementById(this.Id),
                interval_id = setInterval(() => {
                    this.count = 100 - Math.ceil(((i * 10) / this.delay) * 100)
                    el.style.setProperty(this.widthVar,this.count + "%")
                    i++
                }, 10)
            setTimeout(() => {
                el.remove()
                this.Id = null
                clearInterval(interval_id)
            }, this.delay)
        }

    }
}
export  function Success(message) {
    notificationBuilder(message).success().show()
}
export  function Info(message) {
    notificationBuilder(message).info().show()
}
export  function Danger(message) {
    notificationBuilder(message).danger().show()
}
export  function Warning(message) {
    notificationBuilder(message).warning().show()
}
